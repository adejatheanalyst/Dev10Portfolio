SPRING
**Spring Framework**
The original Spring project. Its foundation is dependency management and dependency injection. Building on its dependency capabilities, it provides tools for creating web applications, accessing databases, and managing transactions.
**Spring Data**
Advanced, low-code tools for modeling and accessing databases.
**Spring Security**
Authentication and authorization strategies that can be integrated into any application.
**Spring Boot**
A project entirely dedicated to making the configuration and deployment of Spring projects easier.