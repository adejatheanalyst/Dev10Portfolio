public class Exercise09 {

    public static void main(String[] args) {
        // 1. Write a loop to sum all numbers between 7 and 16.
        int sum = 0;
        // sum all numbers between 7 and 16
        for (int n= 7; n<= 16; n++){
            sum += n;
        }
        // 2. Print the result.
        System.out.println("Sum: " + sum);
    }
}
